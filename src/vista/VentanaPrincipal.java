    private ArrayList<String> validarCantidadNavegadores(){
        ArrayList<String> seleccionados = new ArrayList<String>();
        
        if(checkChrome.isSelected()){
            seleccionados.add(Navegadores.CHROME);
        }        
        if(checkFireFox.isSelected()){
            seleccionados.add(Navegadores.FF);
        }        
        if(checkIE.isSelected()){
            seleccionados.add(Navegadores.IE);
        }        
        if(checkOpera.isSelected()){
            seleccionados.add(Navegadores.OPERA);
        }        
        if (seleccionados.isEmpty() || seleccionados.size() == 1) {
            JOptionPane.showMessageDialog(this,
                    "Debe seleccionar al menos dos opciones.",
                    "Atención",
                    JOptionPane.ERROR_MESSAGE);
            return null;
        
        } else if (seleccionados.size() > 2) {
            JOptionPane.showMessageDialog(this,
                    "No puenden estar seleccionados más de dos navegadores.",
                    "Atención",
                    JOptionPane.ERROR_MESSAGE);
            return null;
        }
        
        return seleccionados;
        
        
        
    }
    
