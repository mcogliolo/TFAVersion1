    private void botonCompararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonCompararActionPerformed
        
       
        //codigo para obtener la URL
        String url = this.urlTextField.getText();
                
        if(url.isEmpty()){
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar una URL válida.",
                    "Atención",
                    JOptionPane.ERROR_MESSAGE);
        
        } else if (url.length() < 10) {
            JOptionPane.showMessageDialog(this,
                    "La URL no es válida.",
                    "Atención",
                    JOptionPane.ERROR_MESSAGE);
        
        } else if(  ((url.substring(0,7)).equals("http://")) ||
                    ((url.substring(0,8)).equals("https://")) )
                {
             
                                    
                    
            String navegador1 =  "";
            String navegador2 = "";
        
            if(validarCantidadNavegadores() != null){
                
                    int respuesta = JOptionPane.showConfirmDialog(null,
                       "Una vez iniciado el proceso de comparación, la aplicación no se"
                               + " detendrá hasta completarlo. Desea continuar?", "Atención",
                     JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                       if (respuesta == JOptionPane.YES_OPTION){
                
                
                
                navegador1 = validarCantidadNavegadores().get(0);
                navegador2 = validarCantidadNavegadores().get(1);
                
                String nombrePrueba = this.nombreTextField.getText();
                
                
                manejador.compararImg(navegador1, navegador2, url, ruta, nombrePrueba);
            }
            }
        } else {
            
            JOptionPane.showMessageDialog(this,
                    "La URL debe contener el prefijo \"http://\" o \"https://\".",
                    "Atención",
                    JOptionPane.ERROR_MESSAGE);  
            
        
        }
        
        
        
        
    }//GEN-LAST:event_botonCompararActionPerformed

